package com.eutools.eueditor.preferences.runtime;

public class RuntimeOverviewPreference {
	private String runtimeName;
	private String runtimeVersion;
	private String runtimeDefault;
	public String getRuntimeName() {
		return runtimeName;
	}
	public void setRuntimeName(String runtimeName) {
		this.runtimeName = runtimeName;
	}
	public String getRuntimeVersion() {
		return runtimeVersion;
	}
	public void setRuntimeVersion(String runtimeVersion) {
		this.runtimeVersion = runtimeVersion;
	}
	public String getRuntimeDefault() {
		return runtimeDefault;
	}
	public void setRuntimeDefault(String runtimeDefault) {
		this.runtimeDefault = runtimeDefault;
	}
	
}
